def _esc(text):
    """Escape special LaTeX characters in user‑supplied text."""
    s = str(text) if text else "---"
    for ch in ['\\', '&', '%', '$', '#', '_', '{', '}', '~', '^']:
        s = s.replace(ch, '\\' + ch)
    return s


