def compile_latex(latex_src):
    """Send LaTeX source to latex.ytotech.com and return (success, data_or_error)."""
    try:
        resp = requests.post(
            "https://latex.ytotech.com/builds/sync",
            json={
                "compiler": "pdflatex",
                "resources": [{"main": True, "content": latex_src}]
            },
            timeout=90,
        )
        if resp.status_code in (200, 201) and resp.content[:5] == b"%PDF-":
            return True, resp.content
        else:
            return False, resp.text[:2000] if resp.text else "Empty response"
    except requests.exceptions.Timeout:
        return False, "Compilation timed out — the service may be busy."
    except Exception as e:
        return False, str(e)


